    private void btn_saleupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saleupdateActionPerformed
        // TODO add your handling code here:
        try{
            String cid = tf_cid.getText();
            String pid= tf_pid.getText();
            String quantity = tf_quantity.getText();
            String total= tf_total.getText();
            Date transdate = dp_sales.getDate();
            if(total.isEmpty()){
                JOptionPane.showMessageDialog(null, "Please Recount Total Price");
                return;
            }
            sm.setCustomer_id(cid);
            sm.setProduct_id(pid);
            sm.setQuantity(Integer.parseInt(quantity));
            sm.setTotal(Integer.parseInt(total));
            sm.setTransaction_date(transdate);

            Boolean result = sc.update(this.id, sm);
            
            String msg = "Update Failed!";
            if (result) {
                msg = "Update Success";
            }

            JOptionPane.showMessageDialog(null, msg);
            updateQuantity();
            this.getSalesHistoryData();
            this.clearSales();
        } catch(Exception e)    {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btn_saleupdateActionPerformed

